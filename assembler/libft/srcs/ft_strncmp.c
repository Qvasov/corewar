#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned char	*str1;
	unsigned char	*str2;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	while (*str1 && *str2 && n)
	{
		if (*str1 != *str2)
			return (*str1 - *str2);
		str1++;
		str2++;
		n--;
	}
	if (n && (*str1 == '\0'))
	{
		if (*str2 == '\0')
			return (0);
		else
			return (-*str2);
	}
	else if (n)
		return (*str1);
	return (0);
}
